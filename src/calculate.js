const calculate = {
  "/": (firstNumber, secondNuumber) => firstNumber / secondNuumber,
  "*": (firstNumber, secondNuumber) => firstNumber * secondNuumber,
  "+": (firstNumber, secondNuumber) => firstNumber + secondNuumber,
  "-": (firstNumber, secondNuumber) => firstNumber - secondNuumber,
  "=": (firstNumber, secondNuumber) => secondNuumber,
};

export default calculate;
